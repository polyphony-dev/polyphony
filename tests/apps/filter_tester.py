@module
class FilterTester:
    def __init__(self):
        self.start = Port(bool, 'in', protocol='ready_valid')
        self.finish = Port(bool, 'out', protocol='ready_valid')
        self.sobel = StreamFilter(W, H, 2)
        self.append_worker(self.test_source)
        self.append_worker(self.test_sink)

    def test_source(self):
        self.start()
        for y in range(H):
            for x in pipelined(range(W)):
                idx = y * W + x
                p = TEST_DATA_61x52[idx]
                #print('in', p)
                self.sobel.inq.wr(p)

    def test_sink(self):
        output:List[int12] = [0] * (W * H)
        for y in range(H):
            for x in pipelined(range(W)):
                idx = y * W + x
                out = self.sobel.outq.rd()
                #print('out', out)
                output[idx] = out
        for i in range(len(output)):
            actual = output[i]
            expected = EXPECTED_DATA_15x4[i]
            print(actual, expected)
            assert actual == expected
        self.finish(True)


